<li>Сложные интерактивные интерфейсы: дашборды, редакторы, таблицы с фильтрацией в реальном времени.</li>
<li>SPA-приложения с авторизацией, где SEO не критично (личные кабинеты, внутренние инструменты).</li>
<li>Проекты, где предполагается долгосрочная разработка большой командой — экосистема и стандарты хорошо работают при масштабировании.</li>
<li>Когда нужна переиспользуемость компонентов между несколькими продуктами.</li>
</ul>

